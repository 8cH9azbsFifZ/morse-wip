	SUBROUTINE AUTOCR (S5, RS)

	DIMENSION S5(512), RS(512), S(1000), RS1(500)
	DATA S/1000*0./, XN/0./
	
	XN=XN+1
	DO 100 I=1, 500
	S(I) = S5(I)
	RS1(I)=0.
100	CONTINUE	

	DO 200 I=1, 500
	DO 300 K=1, 500
	RS1(I)= RS1(I)+S(K+I-1)*S(K)
300	CONTINUE
200	CONTINUE

	DO 400 I=1, 500
	RS(I)=(RS(I)*(XN-1.)+RS1(I))/XN
400	CONTINUE

	RETURN
	END
